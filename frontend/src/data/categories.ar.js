// Renvoie les métadonnées de catégorie selon la langue (fallback FR).
export function localizedCategory(category, locale) {
  if (locale !== "ar") return category;
  const o = CATEGORIES_AR[category.slug];
  return o ? { ...category, name: o.name, seo: o.seo } : category;
}

export function localizedSub(catSlug, sub, locale) {
  if (!sub || locale !== "ar") return sub;
  const o = CATEGORIES_AR[catSlug]?.subcategories?.[sub.slug];
  return o
    ? { ...sub, name: o.name, seo: o.seo, intro: o.intro, faq: o.faq }
    : sub;
}
